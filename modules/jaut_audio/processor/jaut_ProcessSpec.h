#include <juce_audio_processors/juce_audio_processors.h>



namespace jaut
{
    /** A quick wrapper class which gets passed to jaut::ISerialisableAudioProcessor::prepare(ProcessSpec). */
    struct ProcessSpec
    {
        /** The current sample rate. */
        double sampleRate;
        
        /** The amount of samples per audio block. */
        int maxSamplesPerBlock;
        
        /** The bus-layout of the main processor. */
        juce::AudioProcessor::BusesLayout buses;
    };
}
