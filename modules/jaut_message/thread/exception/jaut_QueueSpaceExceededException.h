#pragma once

#include <juce_core/juce_core.h>
#include <system_error>



namespace jaut
{
    /**
     *  Gets thrown inside jaut::MessageHandler if pushing another message could not be done due to lack of space.
     *  One way to overcome this is to increase the size.
     *  
     *  @see jaut::MessageHandler
     */
    class QueueSpaceExceededException : public std::runtime_error
    {
    public:
        explicit QueueSpaceExceededException(const juce::String &message)
            : std::runtime_error(message.toStdString())
        {}
    };
}
